fun main(){
    val marks:Int = 99
    println(marks)

    if (marks < 30){
        println("Failed")
    }
    else if (marks < 50){
        println("Below Average")
    }
    else if (marks < 70){
        println("Average Score")
    }
    else if (marks < 100){
        println("Above Average ( * . * )")
    }
    
    
}